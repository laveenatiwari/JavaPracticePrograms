package defaultpkg;

public class PositiveNegativeExample {

	public static void main(String[] args) {
		int number = -19;
		if (number <0) {
			System.out.println("number provided in input is negative");
		}
		else if (number >0 ) {
			System.out.println("number provided in input is positive");
		}
		else {
			System.out.println("number is zero");
		}
	}
}
